    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // 1. Recoger datos en el objeto Modelo
        Usuario u = new Usuario();
        u.setNombre(txtNombre.getText());
        u.setUsuario(txtUsuario.getText());
        u.setCorreo(txtCorreo.getText());
        u.setPassword(new String(txtPassword.getPassword())); // O txtPassword.getText() si es TextField
        u.setRol(boxRol.getSelectedItem().toString());
        u.setPreguntaSecreta(txtPregunta.getText());
        u.setRespuestaSecreta(txtRespuesta.getText());
// 2. Llamar al servicio
        String mensaje = service.registrarUsuario(u);// 3. Mostrar resultado
        JOptionPane.showMessageDialog(this, mensaje);

        // 4. Si fue exitoso, limpiar
        if (mensaje.contains("correctamente")) {
            limpiar();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    void limpiar() {
        txtNombre.setText("");
        txtUsuario.setText("");
        txtCorreo.setText("");
        txtPassword.setText("");
        txtPregunta.setText("");
        txtRespuesta.setText("");

    }

